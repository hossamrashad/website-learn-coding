function showTime() {
    "use strict";
    let now = new Date();
    // = Get Hours 0-23
    let hours = now.getHours();
    // = Get Minutes 0-59
    let minutes = now.getMinutes();
    // = Get Seconds 0-59
    let seconds = now.getSeconds();
    document.getElementById("clock").textContent =
        hours + ":" + minutes + ":" + seconds;
}
// = Window Event
window.onload = function() {
    "use strict";
    setInterval(showTime, 500);
};